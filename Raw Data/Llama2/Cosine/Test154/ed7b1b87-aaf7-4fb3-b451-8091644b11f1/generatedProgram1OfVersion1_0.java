public class Main {
    public static void main(String[] args) {
        int a = -5 + 8 * 6;
        int b = (55 + 9) % 9;
        int c = 20 + -3 * 5 / 8;
        int d = 5 + 15 / 3 * 2 - 8 % 3;

        System.out.println("a: " + a); // Output: a: 43
        System.out.println("b: " + b); // Output: b: 1
        System.out.println("c: " + c); // Output: c: 19
        System.out.println("d: " + d); // Output: d: 13


    }
}
