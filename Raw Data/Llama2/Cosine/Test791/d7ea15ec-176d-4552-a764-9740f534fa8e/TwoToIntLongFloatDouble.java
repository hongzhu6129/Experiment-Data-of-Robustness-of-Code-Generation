public class TwoToIntLongFloatDouble {

    public static void main(String[] args) {
        int twoAsInt = 2;
        long twoAsLong = 2L;
        float twoAsFloat = 2.0f; // or just 2f
        double twoAsDouble = 2.0d; // or just 2.0 or just 2

        System.out.println("Integer: " + twoAsInt);
        System.out.println("Long: " + twoAsLong);
        System.out.println("Float: " + twoAsFloat);
        System.out.println("Double: " + twoAsDouble);
    }
}
