public class SumOfTwoNumbers {

    public static void main(String[] args) {
        // Declare two integer variables
        int num1 = 74;
        int num2 = 36;

        // Calculate the sum
        int sum = num1 + num2;

        // Print the sum to the console
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);


        // Alternatively, you can directly calculate and print the sum in a single line:
        System.out.println(74 + 36); // Output: 110

        //Or, if you want to explain it further in an educational context:
        System.out.println("Test Data: 74 + 36");
        System.out.println("Expected Output: 110");
        System.out.print("Calculation: ");
        System.out.print(74);
        System.out.print(" + ");
        System.out.print(36);
        System.out.print(" = ");
        System.out.println(74 + 36);

    }
}
