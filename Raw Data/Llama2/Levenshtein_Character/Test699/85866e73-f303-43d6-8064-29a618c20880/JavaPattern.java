public class JavaPattern {

    public static void main(String[] args) {
        System.out.println("j    a   v     v  a");
        System.out.println("j   a a   v   v  a a");
        System.out.println("j  a   a   v v  a   a");
        System.out.println("jj aaaaa   v  aaaaaa");
        System.out.println(" jj a   a   v a   a");
        System.out.println("  j a   a  v  a   a");
        // Note: I've adjusted the spacing in the last line to more closely
        // resemble the intended output given limited formatting control 
        // within the console. It won't be an exact pixel-perfect match 
        // without more advanced formatting techniques not suitable for a 
        // simple console output.  If you needed very precise control, you'd 
        // use a graphical approach (not through System.out.println()).
    }
}
