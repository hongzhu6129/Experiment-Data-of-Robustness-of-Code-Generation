public class Pattern {

    public static void main(String[] args) {
        System.out.println("j        a       (v       a       )");
        System.out.println("j       a a      (v v      a a     )");
        System.out.println("j      a   a     (v   v     a   a    )");
        System.out.println("j     a     a    (v     v    a     a   )");
        System.out.println("j    aaaaaaaaa   (v       v   aaaaaaaaa  )");
        System.out.println("jj   a       a   (v       v   a       a  )");
        System.out.println(" jj  a       a   (v       v   a       a  )");
        System.out.println("  j  a       a    (v     v    a       a   )");
        System.out.println("  j a       a     (v   v     a       a    )");
        System.out.println("   ja       a      (v v      a       a     )");
        System.out.println("   ja       a       (v       a       )");

        // Alternative approach using a single println for better formatting control
        System.out.println("j        a       (v       a       )\n" +
                "j       a a      (v v      a a     )\n" +
                "j      a   a     (v   v     a   a    )\n" +
                "j     a     a    (v     v    a     a   )\n" +
                "j    aaaaaaaaa   (v       v   aaaaaaaaa  )\n" +
                "jj   a       a   (v       v   a       a  )\n" +
                " jj  a       a   (v       v   a       a  )\n" +
                "  j  a       a    (v     v    a       a   )\n" +
                "  j a       a     (v   v     a       a    )\n" +
                "   ja       a      (v v      a       a     )\n" +
                "   ja       a       (v       a       )");


    }
}
