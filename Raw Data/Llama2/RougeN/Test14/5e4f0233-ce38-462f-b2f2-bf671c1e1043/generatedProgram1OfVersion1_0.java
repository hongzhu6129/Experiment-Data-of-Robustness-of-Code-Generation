public class FacePrinter {

    public static void printFace() {
        System.out.println("  +\"\"\"\"+ ");
        System.out.println(" [| o o |]");
        System.out.println("  |  ^  |");
        System.out.println("  | '-' |");
        System.out.println("  +-----+");
    }

    public static void main(String[] args) {
        printFace(); 
    }
}
