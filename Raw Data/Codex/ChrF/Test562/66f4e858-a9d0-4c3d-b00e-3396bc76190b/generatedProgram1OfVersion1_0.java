public class JavaPattern {

    public static void main(String[] args) {
        System.out.println("j    a   v     v  a");
        System.out.println("j   a a   v   v  a a");
        System.out.println("j  a   a   v v  a   a");
        System.out.println("j a     a   v  a     a");
        System.out.println("jaaaaaaaa  v aaaaaaaa");
        System.out.println("j a     a  v a     a");
        System.out.println("j  a   a  v   v a   a");
        System.out.println("j   a a  v     v a a");
        System.out.println("j    a  v       v a");


    }
}
