public class SumOfTwoNumbers {

    public static void main(String[] args) {
        // Declare two integer variables
        int num1 = 74;
        int num2 = 36;

        // Calculate the sum
        int sum = num1 + num2;

        // Print the sum to the console
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);


        // Alternatively, you can do it directly inside the println statement:
        System.out.println("74 + 36: " + (74 + 36)); // Note the parentheses for correct order of operations



    }
}
