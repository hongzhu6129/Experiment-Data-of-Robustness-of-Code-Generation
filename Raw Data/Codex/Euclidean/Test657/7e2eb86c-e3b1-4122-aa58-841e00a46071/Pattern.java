        System.out.println("  j   a a   v   v  a a");
        System.out.println("   j    a   v     v  a");


        // OR if you intend to mirror the provided "a" pattern precisely:

        System.out.println("j    a   v     v  a");
        System.out.println("j   a a   v   v  a a");
        System.out.println("j  a   a   v v  aaaaa");
        System.out.println("j a     a    v  aaaaa");
