public class JavaPattern {

    public static void main(String[] args) {
        System.out.println("j        a        v     v        a        j");
        System.out.println("j       a a        v   v        a a        j");
        System.out.println("j      a   a        v v        a   a        j");
        System.out.println("j     a     a        v        a     a        j");
        System.out.println("jjjjjj aaaaa        v        aaaaaa        jjjjjj");
        System.out.println("                                              ");
        System.out.println("      a        v        a");
        System.out.println("     a a        v        a a");
    }
}
