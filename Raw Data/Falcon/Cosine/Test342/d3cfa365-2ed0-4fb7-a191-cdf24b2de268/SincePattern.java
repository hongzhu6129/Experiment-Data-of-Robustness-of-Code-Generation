public class SincePattern {

    public static void main(String[] args) {
        System.out.println("j     a     v     v     a");
        System.out.println("j    a a    v     v    a a");
        System.out.println("j   a   a    v   v    a   a");
        System.out.println("j  a     a    v v    a     a");
        System.out.println("jj aaaaa a    v    aaaaaa a jj");
        System.out.println("j a     a   v    a     a j");
        System.out.println("j  a   a  v   v  a   a  j");
        System.out.println("j   a a   v     v   a a   j");
        System.out.println("j    a    v     v    a    j");


    }
}
