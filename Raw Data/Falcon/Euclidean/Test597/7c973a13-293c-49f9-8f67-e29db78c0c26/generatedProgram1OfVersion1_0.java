public class ConvertTwo {

    public static void main(String[] args) {
        int twoInt = 2;
        long twoLong = 2L;
        float twoFloat = 2.0f; // or 2f
        double twoDouble = 2.0d; // or 2.0 or just 2


        System.out.println("Integer: " + twoInt);
        System.out.println("Long: " + twoLong);
        System.out.println("Float: " + twoFloat);
        System.out.println("Double: " + twoDouble);
    }
}
