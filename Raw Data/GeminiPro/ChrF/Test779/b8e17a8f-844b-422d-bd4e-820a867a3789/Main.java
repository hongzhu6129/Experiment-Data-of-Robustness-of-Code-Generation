public class Main {
    public static void main(String[] args) {
        // Swapping two equations (demonstrated with variables)
        int a = 10;
        int b = 20;
        System.out.println("Before swap: a = " + a + ", b = " + b);

        int temp = a;
        a = b;
        b = temp;

        System.out.println("After swap: a = " + a + ", b = " + b);


        // Printing a face
        System.out.println("   +\"\"\"\"+   ");
        System.out.println("  | o o |  ");
        System.out.println("  |  ^  |  ");
        System.out.println("  | '-' |  ");
        System.out.println("  +-----+  ");


    }
}
