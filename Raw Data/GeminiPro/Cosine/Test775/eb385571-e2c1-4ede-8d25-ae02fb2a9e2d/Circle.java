public class Circle {

    public static void main(String[] args) {
        double radius = 7.5;
        double perimeter = 2 * Math.PI * radius;
        double area = Math.PI * radius * radius;

        System.out.println("perimeter is = " + perimeter); // Output: perimeter is = 47.12388980384689
        System.out.println("area is = " + area); // Output: area is = 176.71458676442586
    }
}
